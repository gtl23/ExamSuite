    private void updateSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateSubmitButtonActionPerformed
        
        String fName = updateFNameTextField.getText();
        String lName = updateLNameTextField.getText();
        String userName = updateUserNameLabel.getText();
        String dob = updateDayComboBox.getSelectedItem().toString() + " " + 
                    updateMonthComboBox.getSelectedItem().toString() + " " + 
                    updateYearComboBox.getSelectedItem().toString();
        Long mobNo;
        String gender = updateGenderComboBox.getSelectedItem().toString();
        String password = updatePasswordField.getText();
            
        if("".equals(fName) || "".equals(lName) || "".equals(userName) || 
                "".equals(updateMobNoTextField.getText()) || "".equals(password))
            JOptionPane.showMessageDialog(null, "Error, One or more fields are empty!");
        else {
            try {
                mobNo = Long.parseLong(updateMobNoTextField.getText());
                if(updateRePasswordField.getText().equals(password)) {
                    try {
                        FileWriter writer = new FileWriter("C:\\ExamSuite\\Data\\UserDetails\\"+userName+".txt");
                        BufferedWriter bw = new BufferedWriter(writer);
                        
                        bw.write(fName);
                        bw.write(System.getProperty("line.separator"));
                        bw.write(lName);
                        bw.write(System.getProperty("line.separator"));
                        bw.write(userName);
                        bw.write(System.getProperty("line.separator"));
                        bw.write(dob);
                        bw.write(System.getProperty("line.separator"));
                        bw.write(gender);
                        bw.write(System.getProperty("line.separator"));
                        bw.write(mobNo.toString());
                        bw.write(System.getProperty("line.separator"));
                        bw.write(password);
                        bw.write(System.getProperty("line.separator"));
                        
                        bw.flush();
                        
                        clearSignUpForm();
                        
                        CardLayout card = (CardLayout) mainPanel.getLayout();
                        card.show(mainPanel,"profileCard");
                        
                        subWelcomeLabel.setText("Welcome "+fName);
                        
                        updateProfilePanel(userName);
                        updateUpdatePanel(userName);
                        
                        JOptionPane.showMessageDialog(null, "Profile updated successfully.");
                        
                    } catch(Exception e) {
                        JOptionPane.showMessageDialog(this, "Error creating new user account! please check your details...");
                    }
                } else
                    JOptionPane.showMessageDialog(null, "Error, Passwords do not match!");
            }
            catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error, Mobile No. should be of number format only!");
            }
        }
        
    }//GEN-LAST:event_updateSubmitButtonActionPerformed

