    private void signUpCreateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpCreateButtonActionPerformed
    
        String fName = signUpFNameTextField.getText();
        String lName = signUpLNameTextField.getText();
        String userName = signUpUserNameTextField.getText();
        String dob = signUpDayComboBox.getSelectedItem().toString() + " " + 
                    signUpMonthComboBox.getSelectedItem().toString() + " " + 
                    signUpYearComboBox.getSelectedItem().toString();
        Long mobNo;
        String gender = signUpGenderComboBox.getSelectedItem().toString();
        String password = signUpPasswordField.getText();
            
        if("".equals(fName) || "".equals(lName) || "".equals(userName) || 
                "".equals(signUpMobNoTextField.getText()) || "".equals(password))
            JOptionPane.showMessageDialog(null, "Error, One or more fields are empty!");
        else {
            try {
                mobNo = Long.parseLong(signUpMobNoTextField.getText());
                if(checkUserName(userName))
                    if(signUpRePasswordField.getText().equals(password)) {
                        try {
                            FileWriter writer = new FileWriter("C:\\ExamSuite\\Data\\UserDetails\\"+userName+".txt");
                            BufferedWriter bw = new BufferedWriter(writer);
                            
                            bw.write(fName);
                            bw.write(System.getProperty("line.separator"));
                            bw.write(lName);
                            bw.write(System.getProperty("line.separator"));
                            bw.write(userName);
                            bw.write(System.getProperty("line.separator"));
                            bw.write(dob);
                            bw.write(System.getProperty("line.separator"));
                            bw.write(gender);
                            bw.write(System.getProperty("line.separator"));
                            bw.write(mobNo.toString());
                            bw.write(System.getProperty("line.separator"));
                            bw.write(password);
                            bw.write(System.getProperty("line.separator"));
                            
                            bw.flush();
                            
                            clearSignUpForm();
                            
                            CardLayout card = (CardLayout) mainPanel.getLayout();
                            card.show(mainPanel,"subjectCard");
                            
                            subWelcomeLabel.setText("Welcome "+fName);
                            
                            JOptionPane.showMessageDialog(null, "Congrats "+fName+"! you're in.");
                            
                            updateProfilePanel(userName);
                            updateUpdatePanel(userName);
                            
                        } catch(Exception e) {
                            JOptionPane.showMessageDialog(this, "Error creating new user account! please check your details.");
                        }
                    } else
                        JOptionPane.showMessageDialog(null, "Error, Passwords do not match!");
                else
                    JOptionPane.showMessageDialog(null, "Error, Username already exists!");
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error, Mobile No. should be of number format only!");
            }
        }
        
    }//GEN-LAST:event_signUpCreateButtonActionPerformed

    private boolean checkUserName(String userName) {
        try {
            File f = new File("C:\\ExamSuite\\Data\\UserDetails");
            String[] users = f.list();
            for(String userfile : users) {
                String[] user = userfile.split("\\.");
                if(user[0].equals(userName))
                    return false;
            }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Error, "+e.getMessage());
        }
        return true;
    }
    
