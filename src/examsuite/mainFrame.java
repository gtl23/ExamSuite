    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        if("".equals(loginUserNameTextField.getText()))
            JOptionPane.showMessageDialog(null, "Please enter your username.");
        else {
            try {
                String userName = loginUserNameTextField.getText();
                String password = loginPasswordField.getText();
                FileReader reader = new FileReader("C:\\ExamSuite\\Data\\UserDetails\\"+userName+".txt");
                BufferedReader br = new BufferedReader(reader);
                String data;
                ArrayList<String> userData = new ArrayList<>();
                while((data = br.readLine()) != null)
                    userData.add(data);
                if(userData.get(6).equals(password)) {
                    CardLayout card = (CardLayout) mainPanel.getLayout();
                    card.show(mainPanel,"subjectCard");
                    
                    subWelcomeLabel.setText("Welcome "+userData.get(0));
                    
                    loginUserNameTextField.setText("");
                    loginPasswordField.setText("");
                    
                    updateProfilePanel(userName);
                    updateUpdatePanel(userName);
                } else
                    JOptionPane.showMessageDialog(null, "The username and password you entered don't match.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Sorry, ExamSuite doesn't recognize that username.");
            }
        }
    }//GEN-LAST:event_loginButtonActionPerformed

